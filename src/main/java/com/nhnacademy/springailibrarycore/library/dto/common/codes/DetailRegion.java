    public static DetailRegion fromNameAndCity(String name, String cityCode) {
        if (name == null || name.isBlank()) {
            return null;
        }
        String cleanName = name.replaceAll("\\s+", "");
        List<DetailRegion> candidates = Arrays.stream(values())
                .filter(d -> cleanName.contains(d.name))
                .collect(Collectors.toList());
                
        if (candidates.isEmpty()) {
            return null;
        }
        
        if (cityCode != null) {
            return candidates.stream()
                    .filter(d -> d.cityCode.equals(cityCode))
                    .findFirst()
                    .orElse(candidates.get(0));
        }
        
        return candidates.get(0);
    }
